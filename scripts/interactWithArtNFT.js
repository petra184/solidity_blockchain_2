const contractAddress = '0x159563B694A9bE0f98d0645f43056f1c0B15fE86';  

const contract = new web3.eth.Contract(contractABI, contractAddress);

// Function to mint a new art token
async function mintArtToken() {
  const accounts = await web3.eth.requestAccounts();  // Get user's Ethereum account
  
  // Get the data of the art (e.g., metadata, image, etc.)
  const artData = {
    title: 'Artwork Title',
    description: 'A description of the artwork',
    artist: accounts[0],  // the address of the artist
    price: web3.utils.toWei('0.1', 'ether')  // price in Ether
  };

  // Call the minting function of your contract
  contract.methods.mintArt(artData.title, artData.description).send({
    from: accounts[0],
    value: artData.price
  })
  .on('transactionHash', function(hash) {
    console.log('Transaction Hash:', hash);
  })
  .on('receipt', function(receipt) {
    console.log('Minted Art Receipt:', receipt);
    alert('Art minted successfully!');
  })
  .on('error', console.error);
}

// Call the mintArtToken function when user clicks the "Mint Art" button
document.getElementById('mintArtButton').onclick = mintArtToken;
